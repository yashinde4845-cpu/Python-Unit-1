# Creat Tuple
tuple = (3,4,9,6,8)
print("The original Tuple is :",tuple)

# Accessing Element 
print("The first Element is :",tuple[0])
print("The Third Element is :",tuple[2])

# Nested Tuple
Nested_Tup = (1,2,(4,5,7),(6,9,(6,2,1)))
print("The Nested Tuple is :",Nested_Tup)

# Repetation of Tuple 
print("Three times repeated Tuple is :",tuple*3)